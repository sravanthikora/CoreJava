package operators;

public class ternary {
	public static void main(String[] args) {
		int a =10;
		int b = 20;
		System.out.println(a==b? true:false);
		System.out.println(a<b? true:false);
		System.out.println(a>b? true:false);
		System.out.println(a<=b? true:false);
		System.out.println(a>=b? true:false);
	}

}
